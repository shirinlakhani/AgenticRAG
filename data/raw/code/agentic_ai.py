"""
Agentic AI Concepts and Examples
"""

# What is agentic AI
AGENTIC_AI_DEFINITION = """
Agentic AI systems are autonomous agents that can perceive, reason, act, and learn.
Unlike traditional chatbots or GenAI, they take multi-step actions to accomplish goals.
"""

# Core Agentic Loop
CORE_LOOP = """
1. Perceive: Collect data from APIs, databases, or user interactions.
2. Reason & Plan: Use an LLM to break high-level goals into sub-tasks.
3. Act: Execute tasks via tools or API calls.
4. Learn & Adapt: Evaluate outcomes, learn from failures, and improve.
"""

# Technical layers
TECH_ARCHITECTURE = {
    "Tool Layer": "Interfaces with external world: APIs, DBs, knowledge bases",
    "Reasoning Layer": "Core intelligence (LLM) for planning and logic",
    "Memory Layer": "Short-term & long-term memory for context and knowledge",
    "Orchestration Layer": "Manages flow, applies guardrails, HITL checkpoints"
}

# Example: Single-agent vs Multi-agent
AGENT_TYPES = {
    "Single-Agent": "Handles simple sequential tasks",
    "Multi-Agent": "Team of specialized agents for complex parallel tasks"
}
